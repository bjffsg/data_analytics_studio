package com.hortonworks.hivestudio.query.parsers.listener;

import org.antlr.v4.runtime.BaseErrorListener;
import org.antlr.v4.runtime.RecognitionException;
import org.antlr.v4.runtime.Recognizer;
import com.hortonworks.hivestudio.query.exceptions.QueryParseException;

import lombok.extern.slf4j.Slf4j;

/**
 * Error listener for Query parsing error
 */
@Slf4j
public class ParseErrorListener extends BaseErrorListener {
  public static ParseErrorListener INSTANCE = new ParseErrorListener();

  @Override
  public void syntaxError(Recognizer<?, ?> recognizer, Object offendingSymbol, int line, int charPositionInLine, String msg, RecognitionException e) {
    log.error("Failed to parse query at {}:{}. {}", line, charPositionInLine, msg, e);
    QueryParseException queryParseException = new QueryParseException("Failed to parse query. Message: " + msg, e);
    throw queryParseException
      .withAdditionalField("line", line)
      .withAdditionalField("charPosition", charPositionInLine)
      .withAdditionalField("message", msg)
      .withAdditionalField("offendingSymbol", offendingSymbol);
  }
}
